type SteamNetworkingIPAddr struct {
	data [18]byte
}

func (a *SteamNetworkingIPAddr) SetIPv4(ip uint32, port uint16) {
	a.setIPv4(ip, port)
}

func (a *SteamNetworkingIPAddr) SetIPv6(ip [16]byte, port uint16) {
	copy(a.data[:16], ip[:])
	a.data[16] = byte(port)
	a.data[17] = byte(port >> 8)
}

func (a *SteamNetworkingIPAddr) setIPv4(ip uint32, port uint16) {
	a.data[0] = 0
	a.data[1] = 0
	a.data[2] = 0
	a.data[3] = 0
	a.data[4] = 0
	a.data[5] = 0
	a.data[6] = 0
	a.data[7] = 0
	a.data[8] = 0
	a.data[9] = 0
	a.data[10] = 0xff
	a.data[11] = 0xff
	a.data[12] = byte(ip >> 24)
	a.data[13] = byte(ip >> 16)
	a.data[14] = byte(ip >> 8)
	a.data[15] = byte(ip)
	a.data[16] = byte(port)
	a.data[17] = byte(port >> 8)
}

